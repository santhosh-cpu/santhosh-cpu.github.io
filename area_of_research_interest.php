                              <div class="gdlr-core-text-box-item-content" id="div_983a_36">
                                 <p>The study of hydrodynamic stability is an important aspect of the fluid mechanics, largely due to the fact that an unstable flow is not observable and which, in practice, is broken down by “small variations” in flow. It is the task of the stability analyst to identify the critical parameters causing the instability in the flow field. There are two different approaches to address this problem - the first is the linear stability analysis and the second is the non-linear stability analysis.
                                    <br><br>
                                    The linear stability analysis gives a sufficient condition under which the flow is unstable and talks nothing about the stability of the flow field. On the other hand, the nonlinear stability approach (via energy functional) gives a condition under which the flow field is stable but fails to address the instability of the system. In this way these two theories complement each other and resulting into the sub-critical instabilities. Our efforts are to address this phenomenon for a variety of problems both in clear fluids and flows through porous media.
